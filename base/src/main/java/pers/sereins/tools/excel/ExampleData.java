package pers.sereins.tools.excel;

import java.util.Date;

public class ExampleData {

    private String name;
    private Date data;
    private int age;


    public String getName() {
        return name;
    }

    public Date getData() {
        return data;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setData(Date data) {
        this.data = data;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
